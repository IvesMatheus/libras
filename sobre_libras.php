            <h1>O que é?</h1>
            <p>
                <span class="inicio_grande">LIBRAS</span> é uma sigla para Linguagem Brasileira de Sinais reconhecida por Lei (LEI Nº 10.436, DE 24 DE ABRIL  DE 2002). Essa língua serve de comunicação para pessoas que possuem deficiência auditiva.
                <img id="libras_conversacao" src="img/libras_conversacao.jpg" />
                LIBRAS é uma língua gesticulada e não falada. Como outras línguas, possui fonologia, morfologia, sintaxe e semântica. Os itens léxicos que a compõem são os sinais. Esses sinais são formados por uma composição de configuração de mãos, movimentos e de pontos de articulação (locais no espaço ou no corpo onde os sinais são feitos também de expressões faciais e corporais que transmitem sentimentos que para os ouvintes são transmitidos pela entonação da voz, os quais juntos compõem as unidades básicas dessa língua). A LIBRAS, como outras línguas de sinais, não tem um sistema de escrita largamente adotado. Porém possui algumas propostas com a SignWriting. O comum adotado pelos educadores de LIBRAS para representar de forma escrita os sinais é através da escrita em português dos seus significados.
